/*
LTRIM
La fonctionLTRIM()en SQL est une fonction de chaîne qui supprime les espaces de début (à gauche) 
d'une chaîne de caractères . C'est l'opposé de la fonction RTRIM(), qui supprime les espaces de fin (à droite). 
Elle est utilisée pour nettoyer les données et garantir leur uniformité.

LTRIM(chaîne_de_caractères)

*/

SELECT LTRIM('   Bonjour le monde!');
-- Résultat : 'Bonjour le monde!'
